package zach.hw4;

public class Char {
	public String name;
	public int STR;
	public int DEX;
	public int CON;
	public int WIS;
	public int INT;
	public int CHA;

	Char(String name, int STR, int DEX, int CON, int WIS, int INT, int CHA) {
		this.name = name;
		this.STR = STR;
		this.DEX = DEX;
		this.CON = CON;
		this.WIS = WIS;
		this.INT = INT;
		this.CHA = CHA;
	}
}
